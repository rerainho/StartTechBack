public class TerceiraClasse {

    public static void main(String[] args) {

        int numeroA = 10;
        int numeroB = 20;

        int soma = numeroA + numeroB;
        int subtrai = numeroA - numeroB;
        int multiplica = numeroA * numeroB;
        int divide = numeroA / numeroB;

        System.out.println("O resultado da soma é: " + soma);
        System.out.println("A subtração dos números é:" + subtrai);
        System.out.println("Os números multiplicados é igual a:" + multiplica);
        System.out.println("Os números dividios é igual a: " + divide);
    }
}
