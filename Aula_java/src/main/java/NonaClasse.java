public class NonaClasse {

    public static void main(String[] args){
        int multiplicacao = 9;

        //for trata-se de um laço de repetição
        for (int numero = 1; numero <= 10; numero++){//numero++ trata-se de  um incremento)
            System.out.println(multiplicacao + " x " + numero + " = " + numero * multiplicacao);
        }
    }
}
